    <h1>Humanitarna akcija - Pomoć obiteljima u potrebi</h1>
    <p><strong>Datum objave:</strong> 05.05.2024.</p>
    
    <p>Naša škola organizirala je veliku humanitarnu akciju u kojoj su učenici, roditelji i nastavnici zajedničkim snagama prikupljali hranu, odjeću i školski pribor za obitelji u potrebi. Cilj akcije bio je pomoći socijalno ugroženim obiteljima u našem gradu te potaknuti učenike na solidarnost i zajedništvo.</p>
    
    <p>Pripreme su započele nekoliko tjedana ranije, kada su učenici izrađivali plakate i dijelili letke kako bi informirali što više ljudi o ovoj hvalevrijednoj inicijativi. Tijekom akcije, u školskom hodniku postavljeni su punktovi za prikupljanje donacija, a svaki razred je imao zadatak prikupiti određene vrste potrepština, od osnovnih prehrambenih proizvoda do higijenskih potrepština i školskog pribora.</p>

    <p>Zahvaljujući velikodušnosti svih sudionika, prikupili smo više od 200 paketa pomoći koji će biti dostavljeni obiteljima u našem gradu. Osim materijalne pomoći, ova akcija bila je prilika da učenici nauče važnost humanosti i pomaganja drugima. Mnogi su učenici izrazili zadovoljstvo sudjelovanjem, ističući kako su ponosni što su mogli učiniti nešto dobro za svoju zajednicu.</p>

    <p>Volonteri iz viših razreda pomogli su u organizaciji paketa i njihovoj distribuciji, a nekoliko učenika je sudjelovalo i u posjeti obiteljima kako bi osobno uručili donacije. Emotivni trenuci i osmijesi na licima primatelja bili su najveća nagrada za sav trud koji je uložen.</p>

    <p>Ravnatelj škole zahvalio je svim učenicima, roditeljima i nastavnicima na sudjelovanju te istaknuo kako će škola i dalje podržavati slične inicijative u budućnosti. Planiramo organizirati još humanitarnih aktivnosti kako bismo nastavili pomagati onima kojima je to najpotrebnije.</p>

